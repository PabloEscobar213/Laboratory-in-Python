# Блок А — Задача 4
# Дано натуральное число N. Вычислите сумму его цифр.
# Нельзя использовать строки, списки, массивы — только рекурсия и целочисленная арифметика.

def sum_digits(n):
    if n < 10:
        return n
    return n % 10 + sum_digits(n // 10)


# Пример проверки
print("Сумма цифр:", sum_digits(12345))  # 15


# Блок Б — Задача 1
# Вводится последовательность натуральных чисел, завершающаяся 0.
# Найти максимальное число в последовательности.
# Функция сама считывает ввод и возвращает максимум.

def max_sequence():
    s = input().strip()

    # Если строка пустая — продолжаем спрашивать
    if s == "":
        return max_sequence()

    x = int(s)

    if x == 0:
        return 0

    m = max_sequence()
    return x if x > m else m


print("Максимум в последовательности:", max_sequence())

